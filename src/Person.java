public class Person {
    private String name;
    private int id;
    private int age;
    private String group;

    public void setName(String name) {
        this.name = name;
    }

    public String getName() {
        return name;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public int getAge() {
        return age;
    }
    public void setGroup(String group) {
        this.group = group;
    }

    public String getGroup() {
        return group;
    }
    public void setId(int id) {
        this.id = id;
    }

    public int getId() {
        return id;
    }
}
